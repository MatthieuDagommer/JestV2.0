package fr.utt.rt.lo02.projet.modele;

/**
 * Cette enumeration specifie les differentes couleurs que peuvent prendre les
 * cartes Le Coeur, le carreau, le trefle et le Pic sont les seules valeurs
 * possibles pour les cartes
 */

public enum Couleur {

	/** Carte de coeur. */
	COEUR,
	/** Carte de carreau. */
	CARREAU,
	/** Carte de trefle. */
	TREFLE,
	/** Carte de pic. */
	PIC;
}
